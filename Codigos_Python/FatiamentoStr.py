teste = "olá mundo"
#        012345678
# Fatiamento [inicio da string, final da string,passos/quantos caracteres vai 
#             pular a cada print de letra]

print(len(teste))

print(teste[0:9:1]) 
# Vamos pegar do primeiro caractere ao ultimo(Sempre o uma unidade acima do ultimo indice),
# pulando de um em um caractere

print(10*"-")
print(teste[4:])
print(teste[:3])
# Sempre que for começar do primeiro caractere, ou terminar no ultimo, e pular de um em um caractere
# NÃO HÁ A NECESSIDADE DE COLOCAR O INDICE


# FORMA DE INVERTER UMA STRING
print(10*"-")
print(teste[::-1])



